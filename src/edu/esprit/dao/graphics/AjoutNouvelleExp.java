    private boolean isValidDepense() {
        String command = dep.getText();
        if (!command.matches("^[0-9]{1,}[,]?[0-9]{1,}$")) {
            errorDep.setVisible(true);
            return false;
        } else {
            errorDep.setVisible(false);
            return true;
        }
    }

    private boolean isValidTitre() {
        if (titre.getText().length() == 0) {
            errorTitre.setVisible(true);
            return false;
        } else {
            errorTitre.setVisible(false);
            return true;
        }
    }

    private boolean isValidDescrption() {
        if (desc.getText().length() == 0) {
            errorDesc.setVisible(true);
            return false;
        } else {
            errorDesc.setVisible(false);
            return true;
        }
    }

