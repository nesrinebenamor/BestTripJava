            ut = utDAO.rechercherUtilisateurById(id);
            System.out.println(ut.getId());

            Experience exp = new Experience();
            ExperiencesDAO expDAO = new ExperiencesDAO();

            exp.setContenu(desc.getText());
            double d = Double.parseDouble(dep.getText());
            exp.setDepense(d);
            exp.setTitre(titre.getText());
